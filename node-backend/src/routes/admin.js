router.post('/orders/create/', asyncHandler(async (req, res) => {
    const items = Array.isArray(req.body.items) ? req.body.items : [];
    if (!items.length) throw new HttpError(400, 'At least one product is required');
    if (!req.body.customer_name && !req.body.user_id) throw new HttpError(400, 'Customer name or user is required');

    const user = req.body.user_id ? await prisma.user.findUnique({ where: { id: Number(req.body.user_id) } }) : null;
    if (req.body.user_id && !user) throw new HttpError(404, 'User not found');

    const resolvedItems = [];
    let subtotal = new Prisma.Decimal(0);
    for (const [idx, item] of items.entries()) {
        if (!item.product_id) throw new HttpError(400, `Item ${idx}: product_id is required`);
        const product = await prisma.product.findFirst({ where: { id: Number(item.product_id), status: true } });
        if (!product) throw new HttpError(404, `Item ${idx}: Product #${item.product_id} not found`);
        const quantity = Math.max(1, Number(item.quantity || 1));
        const price = decimalInput(item.price, product.price);
        const total = price.mul(quantity);
        subtotal = subtotal.add(total);
        resolvedItems.push({ product, quantity, price, total });
    }

    const shippingCost = decimalInput(req.body.shipping_cost);
    const discountAmount = decimalInput(req.body.discount_amount);
    const grandTotal = Prisma.Decimal.max(subtotal.add(shippingCost).sub(discountAmount), new Prisma.Decimal(0));
    const paidAmount = decimalInput(req.body.paid_amount);
    const dueAmount = Prisma.Decimal.max(grandTotal.sub(paidAmount), new Prisma.Decimal(0));
    const manualNotes = !user && req.body.customer_name ? `[Manual Order] Customer: ${req.body.customer_name}${req.body.customer_email ? ` | Email: ${req.body.customer_email}` : ''}${req.body.customer_phone ? ` | Phone: ${req.body.customer_phone}` : ''}\n` : '';

    const order = await prisma.$transaction(async (tx) => {
        const created = await tx.order.create({
            data: {
                userId: user?.id || req.user.id,
                customerName: req.body.customer_name || '',
                customerEmail: req.body.customer_email || '',
                shippingAddress: req.body.shipping_address || '',
                city: req.body.city || '',
                phone: req.body.customer_phone || '',
                total: grandTotal,
                subtotal,
                discountAmount,
                shippingCost,
                paidAmount,
                dueAmount,
                paymentMethod: req.body.payment_method || 'Cash on Delivery',
                paymentStatus: req.body.payment_status || 'Unpaid',
                orderStatus: req.body.order_status || 'Pending',
                trackingNumber: req.body.tracking_number || '',
                notes: `${manualNotes}${req.body.notes || ''}`.trim(),
                items: { create: resolvedItems.map((item) => ({ productId: item.product.id, quantity: item.quantity, price: item.price, total: item.total })) },
            },
            include: { user: true, coupon: true, items: { include: { product: true } } },
        });
        for (const item of resolvedItems) await tx.product.update({ where: { id: item.product.id }, data: { stock: { decrement: item.quantity } } });
        if (user) await tx.userNotification.create({ data: { userId: user.id, subject: 'New Order Created (Admin)', body: `An order #${created.id} has been created for you by the admin. Total: RS ${Number(grandTotal).toLocaleString()}` } });
        return created;
    });

    res.status(201).json(orderSerializer(order));
}));
