router.get('/analytics/', asyncHandler(async (req, res) => {
    const [orderItems, users, orders] = await Promise.all([
        prisma.orderItem.findMany({ include: { product: { include: { category: true } } } }),
        prisma.user.findMany({ select: { dateJoined: true } }),
        prisma.order.findMany({ select: { createdAt: true } }),
    ]);
    const top = new Map(); const cats = new Map(); const usersByMonth = new Map(); const ordersByMonth = new Map();
    for (const item of orderItems) { const key = item.productId || 0; const cur = top.get(key) || { name: item.product?.name || 'Deleted product', id: key, total_qty: 0, total_revenue: 0 }; cur.total_qty += item.quantity; cur.total_revenue += Number(item.price) * item.quantity; top.set(key, cur); const cat = item.product?.category?.name || 'Uncategorized'; cats.set(cat, (cats.get(cat) || 0) + Number(item.price) * item.quantity); }
    for (const user of users) usersByMonth.set(monthKey(user.dateJoined), (usersByMonth.get(monthKey(user.dateJoined)) || 0) + 1);
    for (const order of orders) ordersByMonth.set(monthKey(order.createdAt), (ordersByMonth.get(monthKey(order.createdAt)) || 0) + 1);
    res.json({ top_products: [...top.values()].sort((a, b) => b.total_qty - a.total_qty).slice(0, 10), category_sales: [...cats.entries()].map(([category, total]) => ({ category, total })), users_per_month: [...usersByMonth.entries()].map(([month, count]) => ({ month, count })), orders_per_month: [...ordersByMonth.entries()].map(([month, count]) => ({ month, count })) });
}));
router.get('/reports/', asyncHandler(async (req, res) => {
    const orders = await prisma.order.findMany();
    const totalRevenue = orders.reduce((sum, o) => sum + Number(o.paidAmount || 0), 0);
    const totalDiscounts = orders.reduce((sum, o) => sum + Number(o.discountAmount || 0), 0);
    const payment = new Map(); const monthly = new Map();
    for (const order of orders) {
        const method = order.paymentMethod || 'Unknown';
        const pm = payment.get(method) || { method, count: 0, total: 0 };
        pm.count += 1; pm.total += Number(order.paidAmount || 0); payment.set(method, pm);
        const month = monthKey(order.createdAt);
        const ms = monthly.get(month) || { month, count: 0, total: 0 };
        ms.count += 1; ms.total += Number(order.paidAmount || 0); monthly.set(month, ms);
    }
    res.json({ total_revenue: totalRevenue, total_discounts: totalDiscounts, total_orders: orders.length, avg_order_value: orders.length ? totalRevenue / orders.length : 0, payment_methods: [...payment.values()], monthly_sales: [...monthly.values()].sort((a, b) => a.month.localeCompare(b.month)) });
}));

