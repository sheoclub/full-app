function slugify(value) {
    return String(value || '').toLowerCase().trim().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '') || `item-${Date.now()}`;
}

async function uniqueProductSlug(name, id) {
    const base = slugify(name);
    let slug = base;
    let counter = 1;
    while (await prisma.product.findFirst({ where: { slug, ...(id ? { NOT: { id } } : {}) } })) slug = `${base}-${counter++}`;
    return slug;
}

function productInclude() {
    return { category: true, brand: true, reviews: true, variants: true, galleryImages: { orderBy: { sortOrder: 'asc' } } };
}

function randomCode(prefix = 'GIFT', length = 12) {
    const alphabet = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
    let code = '';
    while (code.length < length) code += alphabet[crypto.randomInt(0, alphabet.length)];
    return `${prefix}-${code}`;
}

function decimalInput(value, fallback = 0) {
    return new Prisma.Decimal(value === undefined || value === null || value === '' ? fallback : value);
}

async function createCouponFromBody(body, extra = {}) {
    const validDays = Number(body.valid_days || 30);
    const code = body.code || randomCode(body.is_gift || body.generate_token ? 'GIFT' : 'COUPON', Number(body.token_length || 12));
    return prisma.coupon.create({
        data: {
            code: String(code).toUpperCase(),
            discountType: body.discount_type || 'percentage',
            discountValue: decimalInput(body.discount_value),
            minOrderAmount: decimalInput(body.min_order_amount),
            maxUses: Number(body.max_uses || 1),
            isActive: boolFromBody(body.is_active, true),
            validFrom: body.valid_from ? new Date(body.valid_from) : new Date(),
            validTo: body.valid_to ? new Date(body.valid_to) : new Date(Date.now() + validDays * 86400000),
            assignedToId: body.assigned_to ? Number(body.assigned_to) : null,
            isGift: boolFromBody(body.is_gift || body.generate_token, false),
            autoGenerated: boolFromBody(body.auto_generated || body.generate_token || body.bulk_count || body.assign_to_all, false),
            description: body.description || '',
            ...extra,
        },
    });
}

function monthKey(date) {
    const d = new Date(date);
    return `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`;
}

